## Website:: Leetfree
## Link:: https://leetfree.com/problems/word-squares.html
## Topic:: BackTracking
## Sub-topic:: Permutations, Trie
## Difficulty:: Hard
## Approach:: Find all permutations of length K, check if it is a valid square. You can use a trie to find the next possible words given a prefix.
## Time complexity:: O(N * N_P_K)
## Space complexity:: O(N_P_K)
## Notes:: Lazy computation while doing bsearch is very interesting!
## Bookmarked:: Yes
