## Website:: Leetfree
## Link:: https://leetfree.com/problems/plus-one-linked-list.html
## Topic:: Dynamic Programming
## Sub-topic:: 2D
## Difficulty:: Hard
## Approach:: Find the shortest string possible: dp[i, j] = min(dp[i, k] + dp[k+1, j]) OR find repeated strings that completely repeat across [i, j].
## Time complexity:: O(N ^ 4)
## Space complexity:: O(N ^ 2)
