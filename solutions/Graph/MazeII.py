## Website:: LeetFree
## Link:: https://leetfree.com/problems/the-maze.html
## Topic:: Graph
## Sub-topic:: Shortest Path
## Difficulty:: Medium
## Approach:: Construct a weighted graph starting from each position find the single-pair shortest-path
## Time complexity:: O(MN log(MN))
## Space complexity:: O(MN)
## Notes:: You can maintain a min-heap to reduce complexity. Another solution is to use BFS/DFS and keep updating the min distance to the node.
