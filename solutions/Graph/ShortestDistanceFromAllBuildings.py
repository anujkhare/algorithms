## Website:: Leetfree
## Link:: https://leetfree.com/problems/shortest-distance-from-all-buildings.html
## Topic:: Graph
## Sub-topic:: BFS
## Difficulty:: Hard
## Approach:: Start BFS from all the buildings one by one (separately). Accumulate the sum of mindist from each building and the number of reachable buildings. Pick the node which is reachable from all buildings with the mindist.
## Time complexity:: O(MN * MN)
## Space complexity:: O(2*MN)
