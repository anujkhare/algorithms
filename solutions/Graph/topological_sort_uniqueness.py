## Topic:: Graph
## Sub-topic:: Topological sort
## Difficulty:: Medium
## Approach:: Find one topological sort. For each pair of nodes in the sorted output, check if an edge exists between them
## Time complexity:: O(V + E)
## Space complexity:: O(V)
## Notes:: If no edge exists between two adjacent nodes, you could swap them and still be topologically sorted.
