## Website:: Leetfree
## Link:: https://leetfree.com/problems/walls-and-gates.html#approach-2-breadth-first-search-accepted
## Topic:: Graph
## Sub-topic:: BFS
## Difficulty:: Medium
## Approach:: Enqueue all the 0's together at the beginning. Conduct BFS (from all together now!) and prune if there's already a shorter distance path to the node.
## Time complexity:: O(MN)
## Space complexity:: O(MN)
## Notes:: This is better than running BFS one by one from all the nodes.

