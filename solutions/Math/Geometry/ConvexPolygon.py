## Topic:: Math
## Sub-topic:: Geometry
## Difficulty:: Medium
## Approach:: For each vertex, calucate the cross product between the consecutive edges. the direction of the cross product should remain the same for all vertices
## Time complexity:: O(n)
## Space complexity:: O(1)
## Notes:: Cross prouct([a1, b1], [a2, b2]) = a1*b2 - a2*b1
